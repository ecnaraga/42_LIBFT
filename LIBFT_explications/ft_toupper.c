/* *****************************************************************************
 * 
 * Description : Metc en majuscule le char c si ce dernier est une minuscule
 *				 
 * Allocation dynamique : Non
 *
 * Crash :
 *
 * ************************************************************************* */

int	ft_toupper(int c)
{
	if (c >= 97 && c <= 122)
		return (c - 32);
	return (c);
}
