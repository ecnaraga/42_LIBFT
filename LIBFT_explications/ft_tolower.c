#include "libft.h"

/* *****************************************************************************
 * 
 * Description : Metc en miniscule le char c si ce dernier est une majuscule
 *				 
 * Allocation dynamique : Non
 *
 * Crash :
 *
 * ************************************************************************* */

int	ft_tolower(int c)
{
	if (c >= 65 && c <= 90)
		return (c + 32);
	return (c);
}
